def add(n1, n2):
    return n1 + n2
def subraction(n1,n2):
    return n1-n2
def mul(n1,n2):
    return n1*n2
def div(n1,n2):
    return n1/n2

operations={'+':add,'-':subraction,'*':mul,'/':div}
def cal():
    cont=True
    n1=float(input("Enter number one:"))
    while cont:
        for s in operations:
            print(s)
        operation=input("Enter a operator:")
        n2=float(input("Enter number 2:"))
        ans=operations[operation](n1,n2)
        print(f"{n1} {operation} {n2} = {ans}")
        choice=input(f"Enter 'y' to continue with {ans} or 'n' for new calculation")
        if choice=="y":
            n1=ans
        else:
            cont=False
            print("\n"*20)
            cal()
cal()